            <div class="row textveilletech">
                    <h4>De meilleures performances dans les zones denses</h4> <br>
                    <br>
                    La technologie WiFi a tendance à être ralentie lorsque vous êtes dans une zone dense avec beaucoup d’autres appareils connectés autours de vous. <br>
                    Imaginez un aéroport, un centre commercial ou un grand bureau avec tout le monde connecté en WiFi. Il est fort à parier que la connexion sera lente à cause de la congestion ! <br>
                    La nouvelle norme WiFi 6 ou WiFi 802.11ax, comprend beaucoup de nouvelles technologies pour limiter ce problème. <br>
                    Intel annonce que le WiFi 6 devrait améliorer la vitesse de connexion de chaque utilisateur pour être « au moins quatre fois meilleure » dans les zones denses ou avec beaucoup d’appareils connectés.<br> 
                    Cela ne devrait d’ailleurs pas seulement se limiter aux zones denses. Cette technologie pourrait bien, aussi, avoir un effet à la maison si vous avez de nombreux appareils connectés à votre WiFi<br>
                    , ou si vous vivez dans un appartement avec de nombreuses autres connections aux alentours. Congestion et WiFi 6 Vous n’avez pas vraiment besoin de connaître les détails pour comprendre. <br>
                    Un point d’accès Wi-Fi 6 avec un périphérique Wi-Fi 6 fonctionnera mieux. Mais voici ce qui se passe sous le capot : Le WiFi 6 peut désormais diviser un canal WiFi en un grand nombre de sous-canaux.<br> 
                    Chacun de ces sous-canaux peut transporter des données destinées à un appareil différent. C’est possible grâce à la technologie appelée « Orthogonal Frequency Division Multiple Access » ou « OFDMA ». <br>
                    Cela permet au point d’accès WiFi de communiquer à plusieurs appareils simultanément. La nouvelle norme sans pilotes a aussi amélioré le MIMO – Multiple-In, Multiple-Out. <br>
                    Cela implique le routeur dispose de plusieurs antennes pour pouvoir communiquer avec plus d’appareils simultanément. <br>
                    Avec le WiFi 5 le routeur pouvaient déjà communiquer avec plusieurs appareils en même temps, mais ces appareils ne pouvaient pas répondre en même temps. <br>
                    Le WiFi 6 dispose d’une version améliorée du Mu-MIMO qui permet aux périphériques de répondre au point d’accés en même temps. <br>
                    Les points d’accès WiFi proches les uns des autres peuvent générer des congestions sur le réseau s’ils émettent sur le même canal WiFi. <br>
                    Dans ce cas, le routeur écoute et attends d’avoir un canal libre avant de répondre. Avec le WiFi 6, les point d’accès WiFi proches entre eux peuvent être configurés <br>
                    pour avoir différentes couleurs « Basic service set » ou « Ensemble de Service de Base » (BSS). Cette « couleur » est simplement un nombre entre 0 et 7.<br>
                    Lorsqu’un un appareil vérifie si un canal est libre mais capte un faible signal avec une « couleur différente »,<br>
                    il peut alors ignorer ce signal et emmètre quand même sans attendre. Cela devrait améliorer les performances réseau globales et les performances dans les zones denses. <br>
                    Cette technologie est aussi appelée « Spatial frequency re-use » ou « réutilisation des fréquences spatiales ». Ces deux améliorations sont les plus intéressantes concernant le WiFi 6,<br>
                    mais la nouvelle norme prévois aussi de nombreuses petites améliorations. Par exemple le WiFi6, permettra aussi une meilleure utilisation de la technologie Beamforming. <br>
                    Quand pourrez-vous profiter du WiFi 6 ? Certains routeurs disponibles aujourd’hui font déjà leurs promo en annonçant le support WiFi 6 ou 802.11AX, mais la norme WiFi 6 n’est pas encore terminée. <br>
                    De plus il n’existe pas encore d’appareil client (smartphone, tablette, PC…) équipé du WiFi 6 aujourd’hui. <br>
                    L’alliance WiFi prévois et espère que la norme devrait être prête et les premiers appareils devraient faire leurs apparition pendant l’année 2019. <br>
                    En fait, vous ne devriez même pas vraiment vous en soucier – Dans le futur, les nouveaux routeurs, les nouveaux smartphones et les nouveaux PC seront équipés de cette norme WiFi de base. <br>
                    Comme toujours avec les appareils WiFi, il est nécessaire que l’appareil emetteur ET l’appareil récepteur soient équipés de cette nouvelle norme pour bénéficier des performances promises par<br>
                    l’Alliance WiFi. En gros, si vous voulez profiter du WiFi 6 sur votre smartphone, il vous faudra un point d’accès WiFi qui émet en WiFi 6 (routeur WiFi) <br>
                    et un smartphone qui supporte le WiFi 6 également. Si vous connectez un appareil qui ne supporte que la norme WiFi 5 à votre nouveau routeur WiFi 6, cette connexion sera faite en mode WiFi 5. <br>
                    Cependant votre routeur peut continuer à utiliser le WiFi 6 avec un autre appareil simultanément ! <br>
