                <h4>Le WiFi a des numéros de version maintenant</h4><br>
                <br>
                <article>
                    Je vous l’expliquais dans un ancien article, les normes WiFi étaient jusqu’à présent exprimées sous la forme d’un nombre (802.11) suivi d’une ou deux lettres (b, g, n, ac…). <br>
                    Depuis peu, toutes ces normes au noms étranges ont été renommées avec des noms plus compréhensibles, ainsi on se retrouve avec la norme 802.11AC qui deviens le « WiFi 5 ». <br>
                    Voici les versions de la technologie que vous êtes susceptibles de croiser sous peu : <br>
                    WiFi 4 : C’est la norme 802.11n, lancée en 2009. <br>
                    WiFi 5 : C’est la norme 802.11ac, lancée en 2014. <br>
                    WiFi 6 : C’est la nouvelle version du WiFi qui devait s’appeler WiFi 802.11ax. Sa date de sortie est prévue pour 2019. <br>
                    L’alliance WiFi a aussi annoncé qu’ils aimeraient voir apparaître ces nouvelles normes WiFi dans le software. <br>
                    Cela permettrais de savoir quel réseau est plus récent et plus rapide lorsque vous êtes en train de vous connecter avec votre smartphone, votre tablette ou votre PC. <br>
                    Vous pourriez voir apparaître de genre de logo à côté de chaque connexion disponible sous peu… <br>
                    Les version WiFi plus anciennes n’ont pas été officiellement renommées, mais voici ce à quoi elle devraient correspondre : <br>
                    WiFi 1 : Corresponds à la norme 802.11b, lancé en 1999. <br>
                    WiFi 2 : Corresponds à la norme 802.11a, aussi lancée en 1999 <br>
                    WiFi 3 : Corresponds à la norme 802.11g, lancée en 2003 <br>
