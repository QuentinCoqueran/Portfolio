                    <h4>Plus de batterie </h4><br>
                    <br>
                    Un nouveau système de « Target Wake Time » ou « Temps d’Eveil de la Cible » (TWT) est aussi mis en place et signifie que votre smartphone, tablette, <br>
                    PC ou tout autre appareil connecté devrait avoir une meilleure autonomie de batterie. <br>
                    Lorsque le routeur WiFi communique avec un appareil (comme votre smartphone) il peut lui dire exactement quand mettre sa radio WiFi en <br>
                    veille et quand se réveiller pour recevoir la transmission suivante. <br>
                    Cela permettra d’économiser la batterie, la radio WiFi devrait passer plus de temps en veille et donc permettre une durée de vie prolongée pour la batterie. <br>
                    C’est aussi pratique pour les objets connectés récents ayant peu de batterie et qui doivent se connecter en WiFi. <br>
