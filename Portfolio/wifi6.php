                    <h4>Un réseau WiFi plus rapide </h4><br>
                    <br>
                    Comme d’habitude, la nouvelle norme WiFi permet des transferts de données plus rapides. <br>
                    Si vous utilisez un seul appareil avec votre routeur WiFi, les vitesses du WiFi 6 devraient être 40% supérieures aux vitesses de la norme précédente (WiFi 5 = 802.11ac). <br>
                    Pour réussir à atteindre un débit aussi haut, la norme WiFi 6 permet d’encoder les données de manière plus efficace. En gros : plus de données sont encodées dans la même fréquence radio.<br> 
                    Les processeurs qui encodent et décodent ces données deviennent de plus en plus puissants et permettent de supporter la charge de travail supplémentaire. <br>
                    Cette nouvelle norme WiFi permet même d’augmenter le débit sur la bande 2.4 Ghz. <br>
                    Tandis que l’industrie est passée aux fréquences 5Ghz pour éviter les interférences, le WiFi 2.4 Ghz est toujours le meilleur pour traverser les obstacles. <br>
                    De plus, les interférences sur le réseau 2.4 Ghz sont moins importantes aujourd’hui étant donné la disparition des vieux téléphones sans fils qui utilisaient cette fréquence. <br>
                    A lire aussi : WiFi : Definition, historique et tout ce qu’il y a à savoir ? <br>
